// gen-chasm-all generates chasm/lib/all/all.go by scanning chasm/lib subdirectories
// for packages that export a type with a NilLibrary() method. Run via:
//
//	go generate ./chasm/lib/all/...
package main

import (
	"bytes"
	"errors"
	"flag"
	"fmt"
	"go/ast"
	"go/format"
	"go/parser"
	"go/token"
	"log"
	"os"
	"path/filepath"
	"sort"
	"strings"
	"text/template"
)

type libEntry struct {
	ImportPath string
	PkgName    string
}

var allGoTmpl = template.Must(template.New("all").Parse(
	`// Code generated by tools/gen-chasm-all. DO NOT EDIT.

package all

import (
	"go.temporal.io/server/chasm"
{{- range .}}
	{{.PkgName}} "{{.ImportPath}}"
{{- end}}
)

// RegisterAll registers all CHASM libraries in their decoding (nil handler) mode into registry.
// Generated from packages under chasm/lib that export NewNilLibrary().
// To add a new library: implement NewNilLibrary() in your package, then run:
//
//	go generate ./chasm/lib/all/...
func RegisterAll(registry *chasm.Registry) error {
	libs := []chasm.Library{
		&chasm.CoreLibrary{},
{{- range .}}
		{{.PkgName}}.NewNilLibrary(),
{{- end}}
	}
	for _, lib := range libs {
		if err := registry.Register(lib); err != nil {
			return err
		}
	}
	return nil
}
`))

func main() {
	outDir := flag.String("out", ".", "directory to write all.go into")
	libDir := flag.String("lib", "..", "chasm/lib directory to scan")
	flag.Parse()

	if err := run(*outDir, *libDir); err != nil {
		log.Fatal(err)
	}
}

func run(outDir, libDir string) error {
	absLib, err := filepath.Abs(libDir)
	if err != nil {
		return err
	}
	absOut, err := filepath.Abs(outDir)
	if err != nil {
		return err
	}

	modulePath, err := findModulePath(absLib)
	if err != nil {
		return err
	}
	libs, err := scanLibraries(absLib, absOut, modulePath)
	if err != nil {
		return err
	}

	var buf bytes.Buffer
	if err := allGoTmpl.Execute(&buf, libs); err != nil {
		return fmt.Errorf("executing template: %w", err)
	}

	src, err := format.Source(buf.Bytes())
	if err != nil {
		return fmt.Errorf("formatting generated source: %w\n%s", err, buf.String())
	}

	out := filepath.Join(absOut, "all.go")
	if err := os.WriteFile(out, src, 0600); err != nil {
		return fmt.Errorf("writing %s: %w", out, err)
	}
	fmt.Printf("wrote %s\n", out)
	return nil
}

// findModulePath walks up from dir until it finds go.mod and returns the module path declared within.
func findModulePath(dir string) (string, error) {
	for {
		data, err := os.ReadFile(filepath.Join(dir, "go.mod"))
		if err == nil {
			for _, line := range strings.Split(string(data), "\n") {
				if strings.HasPrefix(line, "module ") {
					return strings.TrimSpace(strings.TrimPrefix(line, "module ")), nil
				}
			}
		}
		parent := filepath.Dir(dir)
		if parent == dir {
			return "", errors.New("could not find go.mod")
		}
		dir = parent
	}
}

// scanLibraries returns one entry per subdirectory of libDir that exports NewNilLibrary(),
// sorted by import path for deterministic output.
func scanLibraries(libDir, outDir, modulePath string) ([]libEntry, error) {
	entries, err := os.ReadDir(libDir)
	if err != nil {
		return nil, fmt.Errorf("reading %s: %w", libDir, err)
	}

	var libs []libEntry
	for _, entry := range entries {
		if !entry.IsDir() {
			continue
		}
		absDir, err := filepath.Abs(filepath.Join(libDir, entry.Name()))
		if err != nil || absDir == outDir {
			continue
		}
		pkgName, ok := packageWithNewNilLibrary(absDir)
		if !ok {
			continue
		}
		libs = append(libs, libEntry{
			ImportPath: modulePath + "/chasm/lib/" + entry.Name(),
			PkgName:    pkgName,
		})
	}
	sort.Slice(libs, func(i, j int) bool {
		return libs[i].ImportPath < libs[j].ImportPath
	})
	return libs, nil
}

// packageWithNewNilLibrary parses the Go files in dir and returns the package name
// if the package exports a top-level NewNilLibrary function.
func packageWithNewNilLibrary(dir string) (pkgName string, ok bool) {
	fset := token.NewFileSet()
	pkgs, err := parser.ParseDir(fset, dir, nil, 0)
	if err != nil {
		return "", false
	}
	for name, pkg := range pkgs {
		if strings.HasSuffix(name, "_test") {
			continue
		}
		pkgName = name
		for _, file := range pkg.Files {
			for _, decl := range file.Decls {
				fn, isFn := decl.(*ast.FuncDecl)
				if isFn && fn.Recv == nil && fn.Name.Name == "NewNilLibrary" {
					return pkgName, true
				}
			}
		}
	}
	return "", false
}

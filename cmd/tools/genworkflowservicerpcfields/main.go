package main

import (
	"bytes"
	"flag"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"reflect"
	"sort"
	"strings"

	"go.temporal.io/api/workflowservice/v1"
	"google.golang.org/protobuf/reflect/protoreflect"
	"google.golang.org/protobuf/reflect/protoregistry"
)

const (
	generatedFile          = "workflowservice_rpc_fields_gen.go"
	workflowServiceName    = protoreflect.Name("WorkflowService")
	durationMessageName    = protoreflect.FullName("google.protobuf.Duration")
	maxGeneratedFieldDepth = 3
)

type serviceSpec struct {
	Methods  []methodSpec
	Messages []messageSpec
}

type methodSpec struct {
	Name       string
	InputType  string
	OutputType string
}

type messageSpec struct {
	TypeName string
	Fields   []fieldNode
}

type fieldNode struct {
	Name             string
	Path             string
	SpecType         string
	GoType           string
	DefaultGenerator string
	RefName          string
	Children         []fieldNode
}

type importSet struct {
	aliasesByPath map[string]string
	pathsByAlias  map[string]string
}

func main() {
	out := flag.String("out", ".", "output directory")
	pkg := flag.String("package", "model", "output package")
	flag.Parse()

	if err := run(*out, *pkg); err != nil {
		fmt.Fprintln(os.Stderr, err)
		os.Exit(1)
	}
}

func run(out string, pkg string) error {
	source, err := generate(pkg)
	if err != nil {
		return err
	}
	path := filepath.Join(out, generatedFile)
	if err := os.WriteFile(path, source, 0o644); err != nil {
		return fmt.Errorf("writing %s: %w", path, err)
	}
	return nil
}

func generate(pkg string) ([]byte, error) {
	imports := newImportSet()
	imports.add("go.temporal.io/api/workflowservice/v1", "workflowservice")
	imports.add("go.temporal.io/server/tests/testcore/umpire", "umpire")

	spec, err := workflowServiceSpec(imports)
	if err != nil {
		return nil, err
	}

	var buf bytes.Buffer
	buf.WriteString("// Code generated by cmd/tools/genworkflowservicerpcfields. DO NOT EDIT.\n\n")
	fmt.Fprintf(&buf, "package %s\n\n", pkg)
	writeImports(&buf, imports)
	writeWorkflowServiceSpecTypes(&buf, spec.Messages)
	writeWorkflowServiceRPCSpecs(&buf, spec.Methods)

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return nil, fmt.Errorf("formatting generated source: %w", err)
	}
	return formatted, nil
}

func newImportSet() *importSet {
	return &importSet{
		aliasesByPath: make(map[string]string),
		pathsByAlias:  make(map[string]string),
	}
}

func (s *importSet) add(path string, alias string) string {
	if existing := s.aliasesByPath[path]; existing != "" {
		return existing
	}
	if alias == "" {
		alias = importAliasFromPath(path)
	}
	base := alias
	for i := 2; ; i++ {
		existingPath := s.pathsByAlias[alias]
		if existingPath == "" || existingPath == path {
			break
		}
		alias = fmt.Sprintf("%s%d", base, i)
	}
	s.aliasesByPath[path] = alias
	s.pathsByAlias[alias] = path
	return alias
}

func writeImports(buf *bytes.Buffer, imports *importSet) {
	type importSpec struct {
		Path  string
		Alias string
	}
	specs := make([]importSpec, 0, len(imports.aliasesByPath))
	for path, alias := range imports.aliasesByPath {
		specs = append(specs, importSpec{Path: path, Alias: alias})
	}
	sort.Slice(specs, func(i, j int) bool {
		return specs[i].Path < specs[j].Path
	})
	buf.WriteString("import (\n")
	for _, spec := range specs {
		defaultAlias := importAliasFromPath(spec.Path)
		if spec.Alias != "" && spec.Alias != defaultAlias {
			fmt.Fprintf(buf, "\t%s %q\n", spec.Alias, spec.Path)
			continue
		}
		fmt.Fprintf(buf, "\t%q\n", spec.Path)
	}
	buf.WriteString(")\n\n")
}

func workflowServiceSpec(imports *importSet) (serviceSpec, error) {
	service := workflowservice.File_temporal_api_workflowservice_v1_service_proto.Services().ByName(workflowServiceName)
	if service == nil {
		return serviceSpec{}, fmt.Errorf("service %s not found", workflowServiceName)
	}

	seen := make(map[protoreflect.FullName]messageSpec)
	methods := service.Methods()
	out := serviceSpec{Methods: make([]methodSpec, 0, methods.Len())}
	for i := range methods.Len() {
		method := methods.Get(i)
		inputType, err := goMessageTypeName(method.Input())
		if err != nil {
			return serviceSpec{}, err
		}
		outputType, err := goMessageTypeName(method.Output())
		if err != nil {
			return serviceSpec{}, err
		}
		out.Methods = append(out.Methods, methodSpec{
			Name:       string(method.Name()),
			InputType:  inputType,
			OutputType: outputType,
		})

		for _, descriptor := range []protoreflect.MessageDescriptor{method.Input(), method.Output()} {
			if _, ok := seen[descriptor.FullName()]; ok {
				continue
			}
			typeName, err := goMessageTypeName(descriptor)
			if err != nil {
				return serviceSpec{}, err
			}
			fields, err := collectFields(imports, typeName, descriptor, nil, "")
			if err != nil {
				return serviceSpec{}, fmt.Errorf("collecting fields for %s: %w", descriptor.FullName(), err)
			}
			seen[descriptor.FullName()] = messageSpec{
				TypeName: typeName,
				Fields:   fields,
			}
		}
	}

	out.Messages = make([]messageSpec, 0, len(seen))
	for _, spec := range seen {
		out.Messages = append(out.Messages, spec)
	}
	sort.Slice(out.Messages, func(i, j int) bool {
		return out.Messages[i].TypeName < out.Messages[j].TypeName
	})
	return out, nil
}

func writeWorkflowServiceSpecTypes(buf *bytes.Buffer, messages []messageSpec) {
	for _, message := range messages {
		writeNestedSpecTypes(buf, message.Fields)
	}
	for _, message := range messages {
		fmt.Fprintf(buf, "type %sSpec struct {\n", message.TypeName)
		writeSpecStructFields(buf, "\t", message.Fields)
		buf.WriteString("}\n\n")

		fmt.Fprintf(
			buf,
			"func (%sSpec) RPCMeta() umpire.RPCMessageMeta[*workflowservice.%s] {\n",
			message.TypeName,
			message.TypeName,
		)
		fmt.Fprintf(buf, "\treturn umpire.RPCMessageMeta[*workflowservice.%s]{\n", message.TypeName)
		fmt.Fprintf(buf, "\t\tMessage: &workflowservice.%s{},\n", message.TypeName)
		buf.WriteString("\t\tFields: []umpire.RPCFieldMeta{\n")
		writeFieldMeta(buf, "\t\t\t", message.Fields)
		buf.WriteString("\t\t},\n")
		buf.WriteString("\t}\n")
		buf.WriteString("}\n\n")
	}
}

func writeNestedSpecTypes(buf *bytes.Buffer, fields []fieldNode) {
	for _, field := range fields {
		if len(field.Children) == 0 {
			continue
		}
		writeNestedSpecTypes(buf, field.Children)
		fmt.Fprintf(buf, "type %s struct {\n", field.SpecType)
		fmt.Fprintf(buf, "\t%s umpire.FieldSpec[%s]\n", field.RefName, field.GoType)
		writeSpecStructFields(buf, "\t", field.Children)
		buf.WriteString("}\n\n")
	}
}

func writeSpecStructFields(buf *bytes.Buffer, indent string, fields []fieldNode) {
	for _, field := range fields {
		if len(field.Children) != 0 {
			fmt.Fprintf(buf, "%s%s %s\n", indent, field.Name, field.SpecType)
			continue
		}
		fmt.Fprintf(buf, "%s%s umpire.FieldSpec[%s]\n", indent, field.Name, field.GoType)
	}
}

func writeFieldMeta(buf *bytes.Buffer, indent string, fields []fieldNode) {
	for _, field := range fields {
		fmt.Fprintf(buf, "%s{\n", indent)
		fmt.Fprintf(buf, "%s\tName: %q,\n", indent, field.Name)
		fmt.Fprintf(buf, "%s\tPath: %q,\n", indent, field.Path)
		if field.RefName != "" {
			fmt.Fprintf(buf, "%s\tRefName: %q,\n", indent, field.RefName)
		}
		if field.DefaultGenerator != "" {
			fmt.Fprintf(buf, "%s\tDefaultGenerator: %s,\n", indent, field.DefaultGenerator)
		}
		if len(field.Children) != 0 {
			buf.WriteString(indent + "\tChildren: []umpire.RPCFieldMeta{\n")
			writeFieldMeta(buf, indent+"\t\t", field.Children)
			buf.WriteString(indent + "\t},\n")
		}
		fmt.Fprintf(buf, "%s},\n", indent)
	}
}

func writeWorkflowServiceRPCSpecs(buf *bytes.Buffer, methods []methodSpec) {
	for _, method := range methods {
		fmt.Fprintf(
			buf,
			"func %sRPCSpec(req %sSpec, resp %sSpec) umpire.RPCSpec {\n",
			method.Name,
			method.InputType,
			method.OutputType,
		)
		fmt.Fprintf(
			buf,
			"\treturn umpire.BuildRPCSpec(workflowservice.WorkflowService_%s_FullMethodName, req, resp)\n",
			method.Name,
		)
		buf.WriteString("}\n\n")
	}
}

func collectFields(
	imports *importSet,
	rootType string,
	descriptor protoreflect.MessageDescriptor,
	parentPath []protoreflect.Name,
	specPrefix string,
) ([]fieldNode, error) {
	fields := descriptor.Fields()
	nodes := make([]fieldNode, 0, fields.Len())
	seen := make(map[string]struct{})
	for i := range fields.Len() {
		field := fields.Get(i)
		name := protoFieldName(field)
		if _, ok := seen[name]; ok {
			return nil, fmt.Errorf("duplicate generated field name %s", name)
		}
		seen[name] = struct{}{}

		path := append(append([]protoreflect.Name(nil), parentPath...), field.Name())
		goType, defaultGenerator, err := fieldType(imports, field)
		if err != nil {
			return nil, err
		}
		node := fieldNode{
			Name:             name,
			Path:             fieldPath(path),
			GoType:           goType,
			DefaultGenerator: defaultGenerator,
		}
		if shouldNestField(field, len(path)) {
			specName := rootType + specPrefix + name + "Spec"
			children, err := collectFields(imports, rootType, field.Message(), path, specPrefix+name)
			if err != nil {
				return nil, err
			}
			node.SpecType = specName
			node.RefName = refFieldName(children)
			node.Children = children
		}
		nodes = append(nodes, node)
	}
	return nodes, nil
}

func shouldNestField(field protoreflect.FieldDescriptor, pathLen int) bool {
	return pathLen < maxGeneratedFieldDepth &&
		field.Kind() == protoreflect.MessageKind &&
		!field.IsList() &&
		!field.IsMap() &&
		!isDurationField(field)
}

func fieldType(
	imports *importSet,
	field protoreflect.FieldDescriptor,
) (goType string, defaultGenerator string, err error) {
	if field.IsList() || field.IsMap() {
		return "any", "", nil
	}
	switch field.Kind() {
	case protoreflect.BoolKind:
		return "bool", "", nil
	case protoreflect.EnumKind:
		goType, err := goEnumType(imports, field.Enum())
		if err != nil {
			return "", "", err
		}
		values := enumValues(goType, field.Enum())
		return goType, fmt.Sprintf("umpire.RPCEnumValueGenerator[%s](%s)", goType, strings.Join(values, ", ")), nil
	case protoreflect.Int32Kind, protoreflect.Sint32Kind, protoreflect.Sfixed32Kind:
		return "int32", "", nil
	case protoreflect.Int64Kind, protoreflect.Sint64Kind, protoreflect.Sfixed64Kind:
		return "int64", "", nil
	case protoreflect.Uint32Kind, protoreflect.Fixed32Kind:
		return "uint32", "", nil
	case protoreflect.Uint64Kind, protoreflect.Fixed64Kind:
		return "uint64", "", nil
	case protoreflect.FloatKind:
		return "float32", "", nil
	case protoreflect.DoubleKind:
		return "float64", "", nil
	case protoreflect.StringKind:
		return "string", "", nil
	case protoreflect.BytesKind:
		return "[]byte", "", nil
	case protoreflect.MessageKind, protoreflect.GroupKind:
		if isDurationField(field) {
			imports.add("time", "time")
			return "time.Duration", "umpire.RPCDurationValueGenerator()", nil
		}
		goType, err := goMessageType(imports, field.Message())
		if err != nil {
			return "", "", err
		}
		return goType, "", nil
	default:
		return "any", "", nil
	}
}

func isDurationField(field protoreflect.FieldDescriptor) bool {
	return field.Kind() == protoreflect.MessageKind &&
		field.Message() != nil &&
		field.Message().FullName() == durationMessageName
}

func goEnumType(imports *importSet, descriptor protoreflect.EnumDescriptor) (string, error) {
	enumType, err := protoregistry.GlobalTypes.FindEnumByName(descriptor.FullName())
	if err != nil {
		return "", fmt.Errorf("finding Go enum for %s: %w", descriptor.FullName(), err)
	}
	t := reflect.TypeOf(enumType.New(0))
	alias := imports.add(t.PkgPath(), packageIdent(t))
	return alias + "." + t.Name(), nil
}

func enumValues(goType string, descriptor protoreflect.EnumDescriptor) []string {
	values := descriptor.Values()
	out := make([]string, 0, values.Len())
	for i := range values.Len() {
		out = append(out, fmt.Sprintf("%s(%d)", goType, values.Get(i).Number()))
	}
	return out
}

func goMessageType(imports *importSet, descriptor protoreflect.MessageDescriptor) (string, error) {
	messageType, err := protoregistry.GlobalTypes.FindMessageByName(descriptor.FullName())
	if err != nil {
		return "", fmt.Errorf("finding Go type for %s: %w", descriptor.FullName(), err)
	}
	t := reflect.TypeOf(messageType.Zero().Interface())
	if t.Kind() != reflect.Pointer {
		return "", fmt.Errorf("message type %s is not a pointer", t)
	}
	elem := t.Elem()
	alias := imports.add(elem.PkgPath(), packageIdent(elem))
	return "*" + alias + "." + elem.Name(), nil
}

func goMessageTypeName(descriptor protoreflect.MessageDescriptor) (string, error) {
	messageType, err := protoregistry.GlobalTypes.FindMessageByName(descriptor.FullName())
	if err != nil {
		return "", fmt.Errorf("finding Go type for %s: %w", descriptor.FullName(), err)
	}
	t := reflect.TypeOf(messageType.Zero().Interface())
	if t.Kind() == reflect.Pointer {
		t = t.Elem()
	}
	return t.Name(), nil
}

func protoFieldName(field protoreflect.FieldDescriptor) string {
	parts := strings.Split(string(field.Name()), "_")
	for i, part := range parts {
		parts[i] = fieldNamePart(part)
	}
	return strings.Join(parts, "")
}

func fieldNamePart(part string) string {
	switch strings.ToLower(part) {
	case "api":
		return "API"
	case "id":
		return "ID"
	case "url":
		return "URL"
	case "uri":
		return "URI"
	default:
		if part == "" {
			return ""
		}
		return strings.ToUpper(part[:1]) + part[1:]
	}
}

func fieldPath(path []protoreflect.Name) string {
	parts := make([]string, 0, len(path))
	for _, part := range path {
		parts = append(parts, string(part))
	}
	return strings.Join(parts, ".")
}

func refFieldName(children []fieldNode) string {
	childNames := make(map[string]struct{}, len(children))
	for _, child := range children {
		childNames[child.Name] = struct{}{}
	}
	for _, candidate := range []string{"Ref", "Field", "Self"} {
		if _, ok := childNames[candidate]; !ok {
			return candidate
		}
	}
	for i := 2; ; i++ {
		candidate := fmt.Sprintf("Ref%d", i)
		if _, ok := childNames[candidate]; !ok {
			return candidate
		}
	}
}

func packageIdent(t reflect.Type) string {
	name := t.String()
	name = strings.TrimPrefix(name, "*")
	if idx := strings.LastIndex(name, "."); idx > 0 {
		return name[:idx]
	}
	return importAliasFromPath(t.PkgPath())
}

func importAliasFromPath(path string) string {
	if path == "" {
		return ""
	}
	base := path[strings.LastIndex(path, "/")+1:]
	if base == "v1" {
		parent := path[:strings.LastIndex(path, "/")]
		base = parent[strings.LastIndex(parent, "/")+1:]
	}
	return sanitizeImportAlias(base)
}

func sanitizeImportAlias(alias string) string {
	alias = strings.ReplaceAll(alias, "-", "_")
	alias = strings.ReplaceAll(alias, ".", "_")
	return alias
}
